package com.groupdocs.viewerui.exception;

public class ViewerUiException extends RuntimeException {

	public ViewerUiException(String message) {
		super(message);
	}

	public ViewerUiException(Throwable cause) {
		super(cause);
	}

	public ViewerUiException(String message, Throwable throwable) {
		super(message, throwable);
	}

}
